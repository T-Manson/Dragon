namespace Dragon.Framework.Data.Dapper.Common.Enums
{
    /// <summary>
    /// 表示二元运算的枚举。
    /// </summary>
    public enum BinaryOperation
    {
        /// <summary>
        /// 表示in操作
        /// </summary>
        In,

        /// <summary>
        /// 表示等于运算。
        /// </summary>
        Equal,

        /// <summary>
        /// 表示不等于运算。
        /// </summary>
        NotEqual,

        /// <summary>
        /// 表示大于运算。
        /// </summary>
        Greater,

        /// <summary>
        /// 表示小于运算。
        /// </summary>
        Less,

        /// <summary>
        /// 表示大于或等于。
        /// </summary>
        GreaterOrEqual,

        /// <summary>
        /// 表示小于或等于。
        /// </summary>
        LessOrEqual
    }
}
